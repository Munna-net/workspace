package io.manoj;

public class Manoj {
	
	public int add(int a,int b) {
		return a+b;
	}

	public int multiply(int a,int b) {
		return a*b;
	}
	

	public int sub(int a,int b) {
		return a-b;
	}
	

	public int divide(int a,int b) {
		return a/b;
	}
	
	
	public double computerCircleArea(double radius)
	{
		return Math.PI*radius *radius;
		
	}
	
}
